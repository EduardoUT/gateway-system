    private void validarCategoria(String idCategoria) {
        if(idCategoria.equals("2")) {
            new Facturacion().setVisible(true);
            this.dispose();
        }
    }

    private void validarPerfilUsuario() {
        String nombreUsuario = campoUsuario.getText();
        String password = String.valueOf(campoPassword.getPassword());
        datosPerfil = this.usuarioController.consultarPerfilUsuario(
                nombreUsuario, 
                password
        );
        if (datosPerfil.isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "El usuario o la contrase침a son incorrectos.");
        } else {
            validarCategoria(datosPerfil.get(1));
        }
    }

